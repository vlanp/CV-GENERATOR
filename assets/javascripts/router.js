window.addEventListener("hashchange", () => {
  const hash = window.location.hash.slice(1);
  if (hash === "fr" || hash === "en") {
    currentLanguage = hash;
    updateContent();
    // updateLanguageButtons();
  }
});

window.addEventListener("load", () => {
  const hash = window.location.hash.slice(1);
  if (hash === "fr" || hash === "en") {
    currentLanguage = hash;
  } else {
    window.location.hash = "fr";
  }
  updateContent();
  // updateLanguageButtons();
});
